# 用下列的格式印出每一題的問題及答案

questions = [
    "We don't serve strings around here. Are you a string?",
    "What is said on Father's Day in the forest?",
    "What makes the sound 'Sis! Boom! Bah!'?"
]

answers = [
    "An exploding sheep.",
    "NO, I'm a frayed knot.",
    "'Pop!' goes the weasel."
]

print('Q: %s\nA: %s' % (questions[0], answers[1]))
print('Q: %s\nA: %s' % (questions[1], answers[0]))
print('Q: %s\nA: %s' % (questions[2], answers[2]))